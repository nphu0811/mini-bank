    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // 1. Lấy dữ liệu từ các trường nhập liệu
        String receiverAcStr = adID.getText().trim();
        String amountStr = moneyTrans.getText().trim();

    // Khởi tạo các biến số cần thiết
        int sender_ac = loggedInAcNo;
        int receiver_ac;
        int amount;
    
    // 2. Kiểm tra Rỗng và Chuyển đổi String sang int
        if (receiverAcStr.isEmpty() || amountStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Invaild input", JOptionPane.WARNING_MESSAGE);
            return;
        }
    
        try {
        // Chuyển đổi ID người nhận
            receiver_ac = Integer.parseInt(receiverAcStr);
        // Chuyển đổi Số tiền
            amount = Integer.parseInt(amountStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                                      "Account or amount not availble!. Try again",
                                      "Error format", 
                                      JOptionPane.ERROR_MESSAGE);
            return; // Dừng nếu nhập không phải số
        }
    
    // 3. Kiểm tra logic nghiệp vụ cơ bản
        if (amount <= 0) {
            JOptionPane.showMessageDialog(this, "Amount shoud be more than 0.", "Error transfer", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (receiver_ac == sender_ac) {
            JOptionPane.showMessageDialog(this, "Can't transfer your own account", "Error transfer", JOptionPane.WARNING_MESSAGE);
            return;
        }
            try {
        con.setAutoCommit(false); // Vô hiệu hóa auto-commit
        
        // 4. Kiểm tra số dư người gửi và sự tồn tại của người nhận
        // Truy vấn này đồng thời kiểm tra số dư và đảm bảo người nhận tồn tại
        String checkData = "SELECT balance FROM customer WHERE ac_no = ?";
        
        // Kiểm tra người gửi (số dư)
        PreparedStatement psSender = con.prepareStatement(checkData);
        psSender.setInt(1, sender_ac);
        ResultSet rsSender = psSender.executeQuery();
        
        if (!rsSender.next()) {
             JOptionPane.showMessageDialog(this, "Error : Can't find sender account", "Error system", JOptionPane.ERROR_MESSAGE);
             con.rollback(); 
             return;
        }
        int senderBalance = rsSender.getInt("balance");
        rsSender.close();
        psSender.close();

        if (senderBalance < amount) {
            JOptionPane.showMessageDialog(this, "Balance is not enough to trace");
            con.rollback(); 
            return;
        }

        // Kiểm tra người nhận (đảm bảo tồn tại)
        PreparedStatement psReceiver = con.prepareStatement(checkData);
        psReceiver.setInt(1, receiver_ac);
        ResultSet rsReceiver = psReceiver.executeQuery();
        if (!rsReceiver.next()) {
            JOptionPane.showMessageDialog(this, "Can't found this ID (ID: " + receiver_ac + ").", "Error account!", JOptionPane.WARNING_MESSAGE);
            con.rollback();
            return;
        }
        rsReceiver.close();
        psReceiver.close();

        // 5. Thực hiện giao dịch (Debit & Credit)
        
        // 5a. Trừ tiền người gửi
        String debit = "UPDATE customer SET balance = balance - ? WHERE ac_no = ?";
        PreparedStatement psDebit = con.prepareStatement(debit);
        psDebit.setInt(1, amount);
        psDebit.setInt(2, sender_ac);
        psDebit.executeUpdate();
        psDebit.close();

        // 5b. Cộng tiền người nhận
        String credit = "UPDATE customer SET balance = balance + ? WHERE ac_no = ?";
        PreparedStatement psCredit = con.prepareStatement(credit);
        psCredit.setInt(1, amount);
        psCredit.setInt(2, receiver_ac);
        psCredit.executeUpdate();
        psCredit.close();
        
        // 6. Hoàn tất và thông báo
        con.commit(); // Hoàn tất transaction
        JOptionPane.showMessageDialog(this, "Transfer Succesfully! ", "Succesful", JOptionPane.INFORMATION_MESSAGE);
        
        // Quay về màn hình chính
        new mainDisplay(loggedInAcNo).setVisible(true);
        dispose();
        
    } catch (SQLException e) {
        // Xử lý lỗi Transaction
        try {
            con.rollback(); // Hoàn tác nếu có lỗi
            JOptionPane.showMessageDialog(this, "Error : Cancel trace !", "Error SQL", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, "Rollback failed", ex);
            JOptionPane.showMessageDialog(this, "Hard error system ", "Hard Error", JOptionPane.ERROR_MESSAGE);
        }
        logger.log(java.util.logging.Level.SEVERE, "Transaction error", e);
    } finally {
        // Thiết lập lại auto-commit
        try {
            con.setAutoCommit(true);
        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, "Failed to reset auto-commit", ex);
        }
    }
    }//GEN-LAST:event_confirmBtnActionPerformed

