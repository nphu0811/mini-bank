    private void loadCustomerInfo() {
        String sql = "SELECT cname, balance FROM customer WHERE ac_no = ?";
        
        try (java.sql.PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setInt(1, this.loggedInAcNo);
            
            try (java.sql.ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    String name = rs.getString("cname");
                    int balance = rs.getInt("balance");
                    userName.setText(name);
                    balancefields.setText(""+balance+"");
                }
            }
        } catch (java.sql.SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error when loading customer infor!");
        }
    }
    private void loadReceiverName(int receiverAcNo) {
    // Tránh truy vấn nếu ID là của người gửi
    if (receiverAcNo == this.loggedInAcNo) {
        nameTransfer.setText("Your account");
        return;
    }
    
    String sql = "SELECT cname FROM customer WHERE ac_no = ?";
    
    try (PreparedStatement ps = con.prepareStatement(sql)) {
        ps.setInt(1, receiverAcNo);
        
        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                String name = rs.getString("cname");
                // Tự động điền tên vào trường nameTransfer
                nameTransfer.setText(name); 
            } else {
                // Nếu không tìm thấy ID
                nameTransfer.setText("Invalid account!");
            }
        }
    } catch (SQLException e) {
        // Log lỗi hoặc hiển thị thông báo nhẹ
        logger.log(java.util.logging.Level.SEVERE, "Error loading receiver name", e);
        nameTransfer.setText("Error DB");
    } catch (NumberFormatException e) {
        // Xử lý nếu adID có chữ (mặc dù nên dùng DocumentFilter để cấm chữ)
        nameTransfer.setText("Invaild ID");
    }
}
